// Default context value for SSR / outside-provider usage — English
// t is set to translations.en so page components don't crash during prerender
const LanguageContext = createContext(null);

const VALID_LANGS = ['en', 'he', 'ar', 'ru'];

function detectClientLang() {
  // Only ever called on the client inside useEffect — safe to use localStorage/navigator
  // 1. Respect previously saved user choice
  const saved = localStorage.getItem('jetx_lang');
  if (VALID_LANGS.includes(saved)) return saved;

  // 2. Detect Israel via timezone (primary signal — most reliable)
  try {
    const tz = Intl.DateTimeFormat().resolvedOptions().timeZone;
    if (tz === 'Asia/Jerusalem') return 'he';
  } catch (_) {}

  // 3. Detect Israel via browser locale (secondary signal)
  const locale = (navigator.language || '').toLowerCase();
  if (locale === 'he' || locale.startsWith('he-')) return 'he';

  // 4. Everyone else gets English
  return 'en';
}

export function LanguageProvider({ children }) {
  // Lazy initializer: runs once on first client render (never on server).
  // Reads localStorage synchronously so the correct language is used on the
  // very first paint — no English flash before switching to Hebrew/Arabic/etc.
  // SSR still receives 'en' because the function body never executes server-side
  // (LanguageProvider is a client component and useState lazy init only runs
  // in the browser). Hydration mismatch is suppressed via suppressHydrationWarning
  // on <html> in the root layout.
  const [lang, setLang] = useState(() => {
    if (typeof window === 'undefined') return 'en';
    return detectClientLang();
  });
  const t = translations[lang];

  useEffect(() => {
    document.documentElement.dir = t.dir;
    document.documentElement.lang = lang;
    localStorage.setItem('jetx_lang', lang);
  }, [lang, t.dir]);

  const toggleLang = () => setLang(prev => prev === 'he' ? 'en' : prev === 'en' ? 'ar' : prev === 'ar' ? 'ru' : 'he');

  return (
    <LanguageContext.Provider value={{ lang, setLang, toggleLang, t, isRTL: t.dir === 'rtl' }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const ctx = useContext(LanguageContext);
  // SSR fallback: return English defaults so page components don't crash
  if (!ctx) {
    return {
      lang: 'en',
      setLang: () => {},
      toggleLang: () => {},
      t: translations.en,
      isRTL: false,
    };
  }
  return ctx;
}
export default LanguageContext;
