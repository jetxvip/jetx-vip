// Build lookup index for fast search
const _cache = new Map();

export function searchAirports(query, limit = 8) {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase().trim();

  if (_cache.has(q)) return _cache.get(q);

  const results = [];
  const exact = [];
  const startsWith = [];
  const contains = [];

  for (const [iata, city, country, name] of AIRPORTS_RAW) {
    const iataL = iata.toLowerCase();
    const cityL = city.toLowerCase();
    const nameL = name.toLowerCase();

    if (iataL === q || cityL === q) {
      exact.push({ iata, city, country, name });
    } else if (iataL.startsWith(q) || cityL.startsWith(q)) {
      startsWith.push({ iata, city, country, name });
    } else if (iataL.includes(q) || cityL.includes(q) || nameL.includes(q)) {
      contains.push({ iata, city, country, name });
    }

    if (exact.length + startsWith.length + contains.length >= 50) break;
  }

  const merged = [...exact, ...startsWith, ...contains].slice(0, limit);
  _cache.set(q, merged);
  return merged;
}
